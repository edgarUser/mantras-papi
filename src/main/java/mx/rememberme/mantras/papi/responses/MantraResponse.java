package mx.rememberme.mantras.papi.responses;

public class MantraResponse {

	private String desc;
	private String words;

	public MantraResponse() {

	}

	public String getDesc() {
		return desc;
	}

	public void setDesc(String desc) {
		this.desc = desc;
	}

	public String getWords() {
		return words;
	}

	public void setWords(String words) {
		this.words = words;
	}

}
